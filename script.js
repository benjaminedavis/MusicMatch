var albumPos1 = document.querySelector("#album1");
var albumPos2 = document.querySelector("#album2");
var albumPos3 = document.querySelector("#album3");
var albumPos4 = document.querySelector("#album4");
var audioSource = document.querySelector("#audiosource");
var startButton = document.querySelector("#startbutton");
var currentSong = Math.floor(Math.random() * 4);
var correctWrong = document.querySelector("#correctwrong");
var playerTurn = document.querySelector("#playerturn");
var albumArray = [];
var playerRound = 0;
var player1Score = 0;
var player2Score = 0;

startButton.addEventListener('click', function() {
  randomAlbumArray();
  playerTurn.innerHTML = "Player 1 Turn";
});

function randomAlbumArray() {
  var allAlbumImages = [];
  for (var i = 0; i < allObjects.selections.length; i += 1) {
    allAlbumImages.push(allObjects.selections[i])
  } //returns an array of all albums

  function shuffle(albumImages) {
    //copy is a copy of the master album list
    var copy = [],
      n = albumImages.length,
      i;
    while (n) {
      i = Math.floor(Math.random() * n--);
      copy.push(albumImages.splice(i, 1)[0]);
    }
    copy = copy.slice(0, 4); //rewriting copy array to a 4 element array of random songs
    albumPos1.setAttribute("src", copy[0].albumImage);
    albumPos2.setAttribute("src", copy[1].albumImage);
    albumPos3.setAttribute("src", copy[2].albumImage);
    albumPos4.setAttribute("src", copy[3].albumImage);

    audioSource.setAttribute("src", copy[currentSong].song);
  };
  shuffle(allAlbumImages);
};

function newRandomSong() {
  currentSong = Math.floor(Math.random() * 4);
};

function albumClick(playerSelection) {
  if (currentSong === playerSelection && playerRound <9 && playerRound % 2 === 0) {
    correctWrong.innerHTML = "Correct Answer!";
    playerTurn.innerHTML = "Player 2 Turn";
    playerRound = playerRound +1;
    player2Score = player2Score +1;
    randomAlbumArray();
    newRandomSong();
  } else if (currentSong === playerSelection && playerRound <9) {
      correctWrong.innerHTML = "Correct Answer!";
      playerTurn.innerHTML = "Player 1 Turn";
      playerRound = playerRound +1;
      player1Score = player1Score +1;
      randomAlbumArray();
      newRandomSong();
  } else if (currentSong !== playerSelection && playerRound < 9 && playerRound % 2 === 0) {
    correctWrong.innerHTML = "Wrong Answer!";
    playerTurn.innerHTML = "Player 2 Turn";
    playerRound = playerRound +1;
    randomAlbumArray();
    newRandomSong();
  } else if (currentSong !== playerSelection && playerRound < 9) {
    correctWrong.innerHTML = "Wrong Answer!";
    playerTurn.innerHTML = "Player 1 Turn";
    playerRound = playerRound +1;
    randomAlbumArray();
    newRandomSong();
  } else if (player1Score > player2Score) {
    playerTurn.innerHTML = "Player1 Wins!";
  } else {
    playerTurn.innerHTML = "Player2 Wins!";
  }
};
